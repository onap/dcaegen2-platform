CREATE TABLE IF NOT EXISTS dti.rt_service_instance
(
    SERVICE_INSTANCE_ID             VARCHAR(150) NOT NULL,
    SERVICE_INSTANCE_NAME           VARCHAR(100),
    MODEL_INVARIANT_ID              VARCHAR(150),
    MODEL_VERSION_ID                VARCHAR(100),
    WIDGET_MODEL_ID                 VARCHAR(150),
    WIDGET_MODEL_VERSION            VARCHAR(25),
    BANDWIDTH_TOTAL                 VARCHAR(20),
    BANDWIDTH_UP_WAN1               VARCHAR(20),
    BANDWIDTH_DOWN_WAN1             VARCHAR(20),
    BANDWIDTH_UP_WAN2               VARCHAR(20),
    BANDWIDTH_DOWN_WAN2             VARCHAR(20),
    VHN_PORTAL_URL                  VARCHAR(250),
    SERVICE_INSTANCE_LOCATION_ID    VARCHAR(150),
    RESOURCE_VERSION                VARCHAR(25),
    SELFLINK                        VARCHAR(4000),
    ORCHESTRATION_STATUS            VARCHAR(25),
